class Cuadrado {
    private $lado;
    public function cargarLado($lado){
        $this->lado=$lado;
    }
    public function retornarPerimetro(){
        $perimetro=$this->lado*4;
        return $perimetro;
    }
    public function retornarSuperficie(){
        $superficie=$this->lado*$this->lado;
        return $superficie;
    }
}


